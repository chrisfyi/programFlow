for i in range(0, 12, 2):
    print("i is now {}".format(i))
#range is just like slicing where it doesnt include the last number
# if you dont provide a start it starts at 0
# 2 in this case will count up to the number by 2
# if you want to count backwards you can use a negative number
# start with high number to floor, by -2
for i in range(12, 0, -2):
    print("i is now {}".format(i))

